//---------------------------------------------------------------------------------------------------------------------
// A number expression.
//---------------------------------------------------------------------------------------------------------------------

const Expression = require('./Expression');

//---------------------------------------------------------------------------------------------------------------------

class Num extends Expression
{
    constructor(value)
    {
        super('number');
        this.value = value;
    } // end Num

    toString()
    {
        return this.value.toString();
    } // end toString

    // noinspection JSAnnotator
    eval()
    {
        return this;
    } // end eval
} // end Num
//---------------------------------------------------------------------------------------------------------------------

module.exports = Num;

