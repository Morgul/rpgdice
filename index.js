const { parse } = require('./lib/parser');

//----------------------------------------------------------------------------------------------------------------------

module.exports = {
    parse(expr)
    {
        return parse(expr);
    },
    eval(expr, scope)
    {
        if(typeof expr === 'string')
        {
            expr = parse(expr);
        } // end if

        return expr.eval(scope);
    }
}; // end exports

